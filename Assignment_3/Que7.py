a = 5
print(type(a))
a = 5.5
print(type(a))
a = "Python"
print(type(a))

# The above code depicts Python's Dynamically typed language feture.
# As three different data types are being pointed to same variable name.
# And that variable name is just label which holds three different data types values.
# Complier will predict the data type based on the value assigned to the variable.
# Also the same variable name holds the different data types the memory address of all those will be different.
# If two different variable holds the same data type & same values then moemory address will be same.